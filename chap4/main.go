package main

import "fmt"

func adhocBinding() {
	data := make([]int, 4)

	loopData := func(handleData chan<- int) {
		defer close(handleData)
		for i := range data {
			handleData <- data[i]
		}
	}

	handleData := make(chan int)
	go loopData(handleData)

	for num := range handleData {
		fmt.Println(num)
	}
	//0
	//0
	//0
	//0
}

func main() {
	adhocBinding()
}
